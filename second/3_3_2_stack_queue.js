// stack = 先入れ後出し法
let data = [];
data.push(1);
data.push(2);
data.push(3);
console.log(data.pop(),data);
console.log(data.pop(),data);
console.log(data.pop(),data);

// queue = 先入れ先出し法
data = [];
data.push(1);
data.push(2);
data.push(3);
console.log(data.shift(),data);
console.log(data.shift(),data);
console.log(data.shift(),data);